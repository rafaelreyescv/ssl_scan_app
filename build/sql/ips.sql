create table ips (
Indice int auto_increment primary key,
IP varchar(500) NOT NULL,
Puerto varchar(500) NOT NULL,
SSLv2 BOOLEAN DEFAULT 0,
SSLv3 BOOLEAN DEFAULT 0,
TLSv1_0 BOOLEAN DEFAULT 0,
TLSv1_1 BOOLEAN DEFAULT 0,
TLSv1_2 BOOLEAN DEFAULT 0,
TLSv1_3 BOOLEAN DEFAULT 0,
Fecha TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP);
